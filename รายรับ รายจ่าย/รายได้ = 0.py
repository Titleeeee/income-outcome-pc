import tkinter

รายได้ = 0
ค่าใช้จ่าย = 0

def เพิ่มรายได้():
    global รายได้
    จำนวน = float(input("ป้อนจำนวนรายได้: "))
    รายได้ += จำนวน
    print(f"เพิ่มรายได้ {จำนวน} บาท")
    
def เพิ่มค่าใช้จ่าย():
    global ค่าใช้จ่าย
    จำนวน = float(input("ป้อนจำนวนค่าใช้จ่าย: "))
    ค่าใช้จ่าย += จำนวน
    print(f"เพิ่มค่าใช้จ่าย {จำนวน} บาท")

def แสดงสรุป():
    รายได้สุทธิ = รายได้ - ค่าใช้จ่าย
    print(f"รายได้ทั้งหมด: {รายได้} บาท")
    print(f"ค่าใช้จ่ายทั้งหมด: {ค่าใช้จ่าย} บาท")
    print(f"รายได้สุทธิ: {รายได้สุทธิ} บาท")

while True:
    print("ระบบติดตามรายได้และค่าใช้จ่าย")
    print("1. เพิ่มรายได้")
    print("2. เพิ่มค่าใช้จ่าย")
    print("3. แสดงสรุป")
    print("4. ออก")

    ตัวเลือก = input("โปรดป้อนตัวเลือกของคุณ: ")

    if ตัวเลือก == '1':
        เพิ่มรายได้()
    elif ตัวเลือก == '2':
        เพิ่มค่าใช้จ่าย()
    elif ตัวเลือก == '3':
        แสดงสรุป()
    elif ตัวเลือก == '4':
        print("กำลังออกจากระบบ")
        
        break
    else:
        print("ตัวเลือกไม่ถูกต้อง โปรดลองอีกครั้ง")

